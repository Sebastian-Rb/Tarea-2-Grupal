/*  Requerimientos: Operadores logicos
    Entrada: Datos numeros: 5, 3, 2, 9, 3, 5, 14, 3
    Proceso: Calcular la expresion (5 + 3 * 2) + 9 > 3 * 5 * 14 % 3
    Salida: Mostrar el resultado de la expresion sea verdadero o falso
*/

(5 + 3 * 2) + 9 > 3 * 5 * 14 % 3 
    ? console.log("Verdadero") 
    : console.log("Falso") // Usamos ? y : como una forma de if y else

    